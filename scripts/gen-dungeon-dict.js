/* ============================================================
   GEN-DUNGEON-DICT — builds Dictionary Dungeon's runtime dictionary + rarity
   tiers and writes them to src/data/generated/dungeon-dict.js.

   Dictionary Dungeon (src/games/DictionaryDungeon.jsx) lets the player type ANY
   real word to satisfy a room rule. Unlike Spelldown — which ships per-board
   word arrays so the browser only checks membership — this game can't know the
   answer set ahead of time, so it needs a general dictionary in the browser.

   We keep that payload small the same way Spelldown keeps its data small: bake
   it at build time from the committed ENABLE lists, restrict to the length band
   the game actually uses (3–12 letters — shorter is trivial, longer never comes
   up under our rules), and ship the word set as ONE newline-joined string that
   the runtime splits into a Set once (far smaller than a 150k-element JS array
   literal, and the parser doesn't choke on it).

   RARITY TIERS drive the risk/reward: a word's tier comes from its position in
   google-10000-english (common-10k.txt), which is frequency-ordered:
       • common   — top 2,000            (safe, recognizable)
       • familiar — top 10,000           (normal)
       • obscure  — valid ENABLE, not top-10k
       • goblin   — obscure AND weird-looking (rare-letter-heavy / vowel-starved)
   We ship the two common cutoffs as arrays; obscure/goblin are derived at
   runtime from "in DICT but not in the common sets" + a small heuristic, so we
   don't have to store a per-word tier for 150k words.

   Word source: assets-src/wordlists/<n>.txt (3.txt..12.txt) + common-10k.txt,
   same loader idiom as gen-spelldown.js.

   Run:  npm run gen:dungeon-dict     (no network, no API key)
   ============================================================ */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const WORDLIST_DIR = path.join(ROOT, "assets-src", "wordlists");
const OUT_DIR = path.join(ROOT, "src", "data", "generated");
const OUT_FILE = "dungeon-dict.js";

// The length band the game plays within. 1–2 letter words are trivial/never
// required; 13+ never comes up under our rules and only bloats the payload.
const MIN_LEN = 3;
const MAX_LEN = 12;

// Rarity cutoffs by frequency rank in common-10k.txt (1-based line number).
const COMMON_CUTOFF = 2000; // top 2k = "common"
const FAMILIAR_CUTOFF = 10000; // top 10k = "familiar" (the whole common list)

// A word is "goblin" (weird-looking) if it's obscure AND letter-exotic. The
// runtime re-derives this, but we document the same heuristic here so the check
// script and generator agree on what counts as exotic.
// (kept in sync with dict.js isGoblinShape)

// ── loaders (mirror gen-spelldown.js) ────────────────────────────────────────
function loadEnable() {
  const words = [];
  for (let len = MIN_LEN; len <= MAX_LEN; len++) {
    const file = path.join(WORDLIST_DIR, `${len}.txt`);
    if (!fs.existsSync(file)) continue;
    const list = fs
      .readFileSync(file, "utf8")
      .split(/\r?\n/)
      .map((w) => w.trim().toUpperCase())
      .filter((w) => w && /^[A-Z]+$/.test(w));
    words.push(...list);
  }
  return words;
}

// Ordered top-N common words (uppercased, letters-only), deduped, length-banded
// to match the dictionary. Returns { top2k: [...], top10k: [...] }.
function loadCommon() {
  const file = path.join(WORDLIST_DIR, "common-10k.txt");
  if (!fs.existsSync(file)) {
    throw new Error(
      "common-10k.txt not found in assets-src/wordlists — cannot build rarity tiers."
    );
  }
  const seen = new Set();
  const ordered = [];
  for (const raw of fs.readFileSync(file, "utf8").split(/\r?\n/)) {
    const w = raw.trim().toUpperCase();
    if (!w || !/^[A-Z]+$/.test(w)) continue;
    if (w.length < MIN_LEN || w.length > MAX_LEN) continue;
    if (seen.has(w)) continue;
    seen.add(w);
    ordered.push(w);
  }
  return {
    top2k: ordered.slice(0, COMMON_CUTOFF),
    top10k: ordered.slice(0, FAMILIAR_CUTOFF),
  };
}

// ── build ────────────────────────────────────────────────────────────────────
const enable = loadEnable();
const { top2k, top10k } = loadCommon();

// The shipped dictionary = ENABLE ∪ common (some common words may be shorter
// than MIN_LEN band edges, but they're already filtered to the band above).
// Common words are validated to be real by their presence — but common-10k is a
// curated frequency list and may include a few forms not in ENABLE; we still
// want them playable, so union.
const dictSet = new Set(enable);
for (const w of top10k) dictSet.add(w);

const dict = [...dictSet].sort();
const packed = dict.join("\n");

const banner =
  "// AUTO-GENERATED by scripts/gen-dungeon-dict.js — do not edit by hand.\n" +
  "// Dictionary Dungeon's runtime dictionary + rarity tiers, baked from the\n" +
  "// committed ENABLE lists (assets-src/wordlists/) at build time.\n" +
  `// ${dict.length} words (length ${MIN_LEN}–${MAX_LEN}). Run \`npm run gen:dungeon-dict\` to regenerate.\n`;

const body =
  `${banner}` +
  `export const MIN_LEN = ${MIN_LEN};\n` +
  `export const MAX_LEN = ${MAX_LEN};\n` +
  `export const COMMON_CUTOFF = ${COMMON_CUTOFF};\n` +
  `export const FAMILIAR_CUTOFF = ${FAMILIAR_CUTOFF};\n` +
  `// The dictionary as one newline-joined string; runtime splits into a Set once.\n` +
  `export const DICT_PACKED = ${JSON.stringify(packed)};\n` +
  `// Frequency-ordered common tiers (already length-banded).\n` +
  `export const COMMON_TOP2K = ${JSON.stringify(top2k)};\n` +
  `export const COMMON_TOP10K = ${JSON.stringify(top10k)};\n`;

if (!fs.existsSync(OUT_DIR)) fs.mkdirSync(OUT_DIR, { recursive: true });
fs.writeFileSync(path.join(OUT_DIR, OUT_FILE), body);

const bytes = Buffer.byteLength(body);
console.log(
  `gen-dungeon-dict: ${dict.length} words, top2k=${top2k.length}, top10k=${top10k.length} → ` +
    `src/data/generated/${OUT_FILE} (${(bytes / 1024 / 1024).toFixed(2)} MB)`
);
