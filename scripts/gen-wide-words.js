/* ============================================================
   GEN-WIDE-WORDS — emits the WIDE runtime accept-set to
   src/data/generated/wide-words.js.

   Shared by the daily cabinets where the player TYPES free words and any common
   word that fits should count — currently Chain, Laddergram, and Missing Vowels.
   It's the 4–8-letter slice of the 20k frequency list (google-20k,
   assets-src/wordlists/common-20k.txt): roughly double the 10k `common-words`
   slice, so everyday words the 10k misses (elbow, otter, beige, …) are accepted,
   while staying curated by frequency — these stay games of everyday vocabulary,
   not Scrabble-obscurity contests.

   IMPORTANT: the generators for those cabinets (gen-chain / gen-laddergram /
   gen-vowels) also read common-20k.txt when authoring/validating puzzles, so the
   build-time pool and this runtime accept-set are identical (pars/answers stay
   honest). The narrower `common-words` (10k) artifact still exists and is used by
   the CONTENT-curation generators (Spelldown answer lists, Rank It ranks) — do
   NOT point those here.

   The list is emitted UPPERCASE, sorted, deduped. Consumers do
   `new Set(WIDE_WORDS)` once.

   Run:  npm run gen:wide-words     (no network, no API key)
   ============================================================ */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const WORDLIST_DIR = path.join(ROOT, "assets-src", "wordlists");
const OUT_DIR = path.join(ROOT, "src", "data", "generated");
const OUT_FILE = "wide-words.js";

const MIN_LEN = 4;
const MAX_LEN = 8;

const file = path.join(WORDLIST_DIR, "common-20k.txt");
if (!fs.existsSync(file)) {
  throw new Error(
    "common-20k.txt not found in assets-src/wordlists — fetch " +
      "https://raw.githubusercontent.com/first20hours/google-10000-english/master/20k.txt into that path first."
  );
}

const words = [
  ...new Set(
    fs
      .readFileSync(file, "utf8")
      .split(/\r?\n/)
      .map((w) => w.trim().toUpperCase())
      .filter((w) => w.length >= MIN_LEN && w.length <= MAX_LEN && /^[A-Z]+$/.test(w))
  ),
].sort();

const banner =
  "// AUTO-GENERATED by scripts/gen-wide-words.js — do not edit by hand.\n" +
  "// The WIDE runtime accept-set (4–8 letter words from the 20k frequency list),\n" +
  "// shared by Chain / Laddergram / Missing Vowels. Wider than `common-words`.\n" +
  "// Build a Set once: `new Set(WIDE_WORDS)`. Run `npm run gen:wide-words`.\n";

fs.mkdirSync(OUT_DIR, { recursive: true });
fs.writeFileSync(
  path.join(OUT_DIR, OUT_FILE),
  `${banner}export const WIDE_WORDS = ${JSON.stringify(words)};\nexport default WIDE_WORDS;\n`
);

console.log(`\n✓ wrote ${words.length} wide words (${MIN_LEN}–${MAX_LEN} letters) → src/data/generated/${OUT_FILE}\n`);
