/* ============================================================
   GEN-COMMON-WORDS — emits the compact runtime dictionary the new daily word
   cabinets validate against, to src/data/generated/common-words.js.

   Several cabinets (Laddergram, Chain, Missing Vowels) must validate ARBITRARY
   player input in the browser, so they need a word Set at runtime. We ship the
   4–8-letter slice of the curated common-words list (google-10000-english,
   assets-src/wordlists/common-10k.txt) — a few thousand everyday words — instead
   of the 173k ENABLE list. This keeps the "browser never sees the giant
   dictionary" property Spelldown established, and one artifact serves all three
   cabinets.

   The list is emitted UPPERCASE, sorted, deduped. Consumers do
   `new Set(COMMON_WORDS)` once. common-10k is frequency-ordered but ORDER DOESN'T
   MATTER here (membership only) — Rank It, which DOES need frequency rank, reads
   common-10k directly in its own generator.

   Run:  npm run gen:common-words     (no network, no API key)
   ============================================================ */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const WORDLIST_DIR = path.join(ROOT, "assets-src", "wordlists");
const OUT_DIR = path.join(ROOT, "src", "data", "generated");
const OUT_FILE = "common-words.js";

const MIN_LEN = 4;
const MAX_LEN = 8;

const file = path.join(WORDLIST_DIR, "common-10k.txt");
if (!fs.existsSync(file)) {
  throw new Error(
    "common-10k.txt not found in assets-src/wordlists — fetch " +
      "google-10000-english-no-swears.txt into that path first."
  );
}

const words = [
  ...new Set(
    fs
      .readFileSync(file, "utf8")
      .split(/\r?\n/)
      .map((w) => w.trim().toUpperCase())
      .filter((w) => w.length >= MIN_LEN && w.length <= MAX_LEN && /^[A-Z]+$/.test(w))
  ),
].sort();

const banner =
  "// AUTO-GENERATED by scripts/gen-common-words.js — do not edit by hand.\n" +
  "// The compact runtime dictionary (4–8 letter common words) the daily word\n" +
  "// cabinets validate against. Build a Set once: `new Set(COMMON_WORDS)`.\n" +
  "// Run `npm run gen:common-words` to regenerate from assets-src/wordlists/.\n";

fs.mkdirSync(OUT_DIR, { recursive: true });
fs.writeFileSync(
  path.join(OUT_DIR, OUT_FILE),
  `${banner}export const COMMON_WORDS = ${JSON.stringify(words)};\nexport default COMMON_WORDS;\n`
);

console.log(`\n✓ wrote ${words.length} common words (${MIN_LEN}–${MAX_LEN} letters) → src/data/generated/${OUT_FILE}\n`);
